def generate_all_posters():
    """生成所有影视的海报"""
    from app import create_app
    from app.models import Movie
    from app.extensions import db
    
    app = create_app()
    ctx = app.app_context()
    ctx.push()
    
    movies = Movie.query.all()
    print(f"开始为 {len(movies)} 部影视生成海报...")
    print("=" * 100)
    
    generated = 0
    failed = []
    
    for m in movies:
        movie_id = m.id
        title = m.title
        
        if movie_id not in POSTER_PROMPTS:
            print(f"[{movie_id}] {title} - ⚠️ 缺少提示词")
            failed.append(title)
            continue
        
        prompt = POSTER_PROMPTS[movie_id]
        
        print(f"[{movie_id}] {title}")
        print(f"    提示词: {prompt[:60]}...")
        
        # 这里需要调用 image_gen 工具
        # 由于我们是在脚本中，无法直接调用工具
        # 需要改为在外部循环中调用
        
        print(f"    状态: 需要外部生成")
        print("-" * 100)
    
    ctx.pop()
    print(f"\n生成完成: {generated} 成功, {len(failed)} 失败")
    
    if failed:
        print(f"\n⚠️ 以下 {len(failed)} 部影视缺少提示词:")
        for f in failed:
            print(f"  - {f}")

if __name__ == '__main__':
    generate_all_posters()
